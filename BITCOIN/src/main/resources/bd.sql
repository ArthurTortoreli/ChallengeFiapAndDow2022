SET DATABASE SQL SYNTAX ORA TRUE;

CREATE TABLE "TB_USUARIOS" (
    "ID" NUMERIC(15) PRIMARY KEY,
    "NOME" VARCHAR(50) NOT NULL,
    "SENHA" VARCHAR(150) NOT NULL,
    "CPF" VARCHAR(20) NOT NULL,
    "NIVEL" NUMERIC(5) NOT NULL,
    "PONTOS_ACUMULADOS" NUMERIC(15),
    "EXPERIENCIA" NUMERIC(15)
);

CREATE TABLE "USERS" (
    "USERNAME" VARCHAR(150) PRIMARY KEY,
    "PASSWORD" VARCHAR(150),
    "ENABLED" VARCHAR(5)
);

CREATE TABLE "AUTHORITIES" (
    "USERNAME" VARCHAR(150) PRIMARY KEY,
    "AUTHORITY" VARCHAR(10)
);

CREATE TABLE "TB_RECICLAGEM" (
    "ID" NUMERIC(15) PRIMARY KEY,
    "ID_FUNCIONARIO" NUMERIC(15) NOT NULL,
    "ID_PONTO_DE_RECICLAGEM" NUMERIC(15) NOT NULL,
    "MATERIAL" VARCHAR(50),
    "QUANTIDADE" NUMERIC(15,2),
    "PONTOS_GERADOS" NUMERIC(15),
    "DATA" DATE
);

CREATE TABLE "TB_PONTOS_DE_RECICLAGEM" (
    "ID" NUMERIC(15) PRIMARY KEY,
    "NOME" VARCHAR(150),
    "CNPJ" VARCHAR(20)
);

CREATE TABLE "TB_PRODUTOS" (
    "ID" NUMERIC(15) PRIMARY KEY,
    "NOME" VARCHAR(150),
    "VALOR_PONTOS" NUMERIC(15),
    "URL_IMAGEM" VARCHAR(300)
);

CREATE TABLE "TB_HISTORICO_DE_COMPRAS" (
    "ID" NUMERIC(15) PRIMARY KEY,
    "PRODUTO" VARCHAR(150),
    "PONTOS" NUMERIC(15),
    "USUARIO" NUMERIC(15)
);

CREATE SEQUENCE SQ_ID_FUNCIONARIO START WITH 10 INCREMENT BY 10;
CREATE SEQUENCE SQ_ID_RECICLAGEM START WITH 10 INCREMENT BY 1;
CREATE SEQUENCE SQ_HISTORICO_DE_COMPRAS START WITH 10 INCREMENT BY 1;

